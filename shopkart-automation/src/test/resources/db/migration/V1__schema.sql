CREATE TABLE IF NOT EXISTS customers (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  persona VARCHAR(30) NOT NULL UNIQUE,
  email VARCHAR(120) NOT NULL UNIQUE,
  password_hash VARCHAR(255) NOT NULL DEFAULT 'ENV_REQUIRED',
  display_name VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS products (
  sku VARCHAR(30) PRIMARY KEY,
  name VARCHAR(120) NOT NULL,
  description VARCHAR(500) NOT NULL,
  category VARCHAR(60) NOT NULL,
  price_paise INTEGER NOT NULL CHECK (price_paise > 0),
  stock INTEGER NOT NULL CHECK (stock >= 0),
  image_key VARCHAR(80) NOT NULL
);

CREATE TABLE IF NOT EXISTS carts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_id BIGINT NOT NULL REFERENCES customers(id),
  status VARCHAR(20) NOT NULL DEFAULT 'OPEN' CHECK (status IN ('OPEN', 'ORDERED')),
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_carts_customer_status ON carts(customer_id, status);

CREATE TABLE IF NOT EXISTS cart_items (
  cart_id BIGINT NOT NULL REFERENCES carts(id) ON DELETE CASCADE,
  sku VARCHAR(30) NOT NULL REFERENCES products(sku),
  qty INTEGER NOT NULL CHECK (qty > 0),
  unit_price_paise INTEGER NOT NULL CHECK (unit_price_paise > 0),
  PRIMARY KEY (cart_id, sku)
);

CREATE TABLE IF NOT EXISTS orders (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 7001) PRIMARY KEY,
  customer_id BIGINT NOT NULL REFERENCES customers(id),
  cart_id BIGINT NOT NULL UNIQUE REFERENCES carts(id),
  status VARCHAR(20) NOT NULL DEFAULT 'PLACED' CHECK (status IN ('PLACED', 'CANCELLED')),
  total_paise INTEGER NOT NULL CHECK (total_paise > 0),
  address VARCHAR(240) NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX IF NOT EXISTS idx_orders_customer ON orders(customer_id);

CREATE TABLE IF NOT EXISTS order_items (
  order_id BIGINT NOT NULL REFERENCES orders(id) ON DELETE CASCADE,
  sku VARCHAR(30) NOT NULL,
  name VARCHAR(120) NOT NULL,
  qty INTEGER NOT NULL CHECK (qty > 0),
  unit_price_paise INTEGER NOT NULL CHECK (unit_price_paise > 0),
  line_total_paise INTEGER NOT NULL CHECK (line_total_paise > 0),
  PRIMARY KEY (order_id, sku)
);
